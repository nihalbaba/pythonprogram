#example program for bitwise oprator
print("bitwise oprator ")
a=int(input("enter the value of a"))# enter any value of a
b=int(input("enter the value of b")) # enter any value of b
R=a&b #the AND opration of a and b
print("the AND opration :",R)
R=a|b #the OR opration of a and b
print("the OR opration :",R)
R=a^b #the XOR opration of a and b
print("the XOR opration :",R)
R=~a #the COMPLEMENT opration of a and b
print("the complement opration of a :",R)



